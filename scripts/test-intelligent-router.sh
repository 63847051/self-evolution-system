#!/bin/bash
# 测试智能消息路由系统
#
# 版本: v1.0
# 创建时间: 2026-03-12

set -e

WORKSPACE="/root/.openclaw/workspace"
ROUTER="$WORKSPACE/scripts/intelligent-message-router.sh"

echo "=================================================="
echo "🧪 智能消息路由系统 - 测试套件"
echo "=================================================="
echo ""

# ============================================================================
# 测试函数
# ============================================================================

run_test() {
    local test_name="$1"
    local test_message="$2"
    local test_sender="test_user"
    local test_chat_id="test_chat"
    local test_chat_type="group"

    echo "🧪 $test_name"
    echo "--------------------------------------------------"
    echo "📨 测试消息:"
    echo "$test_message"
    echo ""
    echo "🔄 执行中..."
    echo ""

    if [[ -x "$ROUTER" ]]; then
        "$ROUTER" "$test_message" "$test_sender" "$test_chat_id" "$test_chat_type"
        echo "✅ 测试完成"
    else
        echo "❌ 测试失败: 路由器不可执行"
        return 1
    fi

    echo ""
    echo "=================================================="
    echo ""
}

# ============================================================================
# 测试用例
# ============================================================================

# 测试 1: 单一设计任务
run_test "测试 1: 单一设计任务" \
    "帮我设计一个客厅"

# 测试 2: 单一技术任务
run_test "测试 2: 单一技术任务" \
    "写个脚本抓取网页数据"

# 测试 3: 单一日志任务
run_test "测试 3: 单一日志任务" \
    "更新工作日志"

# 测试 4: 设计 + 技术并行 ⭐
run_test "测试 4: 设计 + 技术并行" \
    "帮我设计客厅并写个脚本统计面积"

# 测试 5: 设计 + 技术 + 日志并行 ⭐⭐
run_test "测试 5: 设计 + 技术 + 日志并行" \
    "设计一个会议室，写个脚本统计数据，然后记录到工作日志"

# 测试 6: 协调任务
run_test "测试 6: 协调任务" \
    "帮我分配这些任务：设计客厅、编写脚本、更新日志"

# 测试 7: 复杂并行任务 ⭐⭐⭐
run_test "测试 7: 复杂并行任务" \
    "今天要完成以下工作：
    1. 修改3F更衣室排砖平面图
    2. 编写Python脚本分析数据
    3. 更新所有任务的状态到日志
    请协调处理"

# 测试 8: 通用任务
run_test "测试 8: 通用任务" \
    "你好"

echo "=================================================="
echo "🎉 所有测试完成！"
echo "=================================================="
echo ""
echo "📁 查看详细日志:"
echo "  ls -la $WORKSPACE/logs/intelligent-router/"
echo "  cat $WORKSPACE/logs/intelligent-router/router_*.log"
echo ""
